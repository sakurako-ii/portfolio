    baseSpeed = minBaseSpeed + (baseSpeed - minBaseSpeed) * spreadMultiplier;

    // サイズ依存の速度
    const speedMultiplier = size >= 80 ? 2.0 : 1.0;
    const vx = baseSpeed * speedMultiplier * (Math.random() < 0.5 ? -1 : 1);
    const vy = baseSpeed * speedMultiplier * (Math.random() < 0.5 ? -1 : 1);

    shapes.push({ el: img, x, y, vx, vy, size, side });
  }

  // 左右に5個ずつ（合計10個）
  for (let i = 0; i < numShapes; i++) {
    createShape("left", i);
    createShape("right", i + 5);
  }

  function animate() {
    const scrollBarWidth = window.innerWidth - document.documentElement.clientWidth;

  // 画面幅から900pxとスクロールバーを引いた残りの幅
  const usableWidth = window.innerWidth - 900 - scrollBarWidth;

    shapes.forEach((s) => {
      s.x += s.vx;
      s.y += s.vy;

      // 上下境界
      if (s.y <= 0) { s.vy = Math.abs(s.vy) * (0.8 + Math.random() * 0.4);
      s.vx = s.vx * (0.8 + Math.random() * 0.4);
      s.y = 0;
      }

      if (s.y + s.size >= window.innerHeight) {
        s.vy = -Math.abs(s.vy)* (0.8 + Math.random() * 0.4);
      s.vx = s.vx * (0.8 + Math.random() * 0.4);
      s.y = window.innerHeight - s.size; 
      }

      if (s.side === "left") {
        const leftMaxX = usableWidth / 2 - s.size;
        if (s.x <= 0) {
            s.vx = Math.abs(s.vx) * (0.8 + Math.random() * 0.4);
            s.vy = s.vy * (0.8 + Math.random() * 0.4);
            s.x = 0; 
      }
        if (s.x >= leftMaxX) {
            s.vx = -Math.abs(s.vx) * (0.8 + Math.random() * 0.4);
            s.vy = s.vy * (0.8 + Math.random() * 0.4);
            s.x = leftMaxX; 
      }
     } else {
        const rightMinX = window.innerWidth - usableWidth / 2 - 10;
        const rightMaxX = window.innerWidth - scrollBarWidth - s.size;
        if (s.x <= rightMinX) {
        s.vx = Math.abs(s.vx) * (0.8 + Math.random() * 0.4);
        s.vy = s.vy * (0.8 + Math.random() * 0.4);
        s.x = rightMinX; 
      }
        if (s.x >= rightMaxX) {
        s.vx = -Math.abs(s.vx) * (0.8 + Math.random() * 0.4);
        s.vy = s.vy * (0.8 + Math.random() * 0.4);
        s.x = rightMaxX; 
      }
    }

      s.el.style.left = `${s.x}px`;
      s.el.style.top = `${s.y}px`;
    });

    requestAnimationFrame(animate);
  }

